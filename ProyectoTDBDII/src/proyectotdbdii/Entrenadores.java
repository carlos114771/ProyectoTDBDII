package proyectotdbdii;

import java.util.Date;

/**
 *
 * @author Usuario Dell
 */
public class Entrenadores extends Persona {

    public Entrenadores() {
    }

    public Entrenadores(int id, String nombre, String apellido, String genero, Date fecha_nacimiento) {
        super(id, nombre, apellido, genero, fecha_nacimiento);
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public Date getFecha_nacimiento() {
        return fecha_nacimiento;
    }

    public void setFecha_nacimiento(Date fecha_nacimiento) {
        this.fecha_nacimiento = fecha_nacimiento;
    }

}
